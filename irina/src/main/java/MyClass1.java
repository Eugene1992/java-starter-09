public class MyClass1 {
    public static void main (String[] args){
        int count = 0;

        System.out.println("Start");

        while (count <= 10){
            System.out.println(count);
            count++;
        }
        System.out.println("End");

        count = 10;
        System.out.println("Start");

        while (count >0){
            System.out.println(count);
            count--;
        }
        System.out.println("End");

    }
}
